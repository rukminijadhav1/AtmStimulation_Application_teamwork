package atmStimulationTeamWork;

public class AtmEntity {
	private int cardNumber;
	private double balance;
	private double withdrawlAmount;
	private double depositeAmount;
	private double fastCashAmount;
	public int getCardNumber() {
		return cardNumber;
	}
	public void setCardNumber(int cardNumber) {
		this.cardNumber = cardNumber;
	}
	public double getBalance() {
		return balance;
	}
	public void setBalance(double balance) {
		this.balance = balance;
	}
	public double getWithdrawlAmount() {
		return withdrawlAmount;
	}
	public void setWithdrawlAmount(double withdrawlAmount) {
		this.withdrawlAmount = withdrawlAmount;
	}
	public double getDepositeAmount() {
		return depositeAmount;
	}
	public void setDepositeAmount(double depositeAmount) {
		this.depositeAmount = depositeAmount;
	}
	public double getFastCashAmount() {
		return fastCashAmount;
	}
	public void setFastCashAmount(double fastCashAmount) {
		this.fastCashAmount = fastCashAmount;
	}
	

}


